#include <iostream>
using namespace std;

int main()
{
    int a = 3, b = 4;
    cout << "a+b  " <<"= "<< a + b<<endl;
    cout << "a-b  " <<"= "<< a - b<<endl;
    cout << "a*b  " <<"= "<< a * b<<endl;
    cout << "a/b  " <<"= "<< a / b<<endl;
    cout << "a%b  " <<"= "<< a % b<<endl;
    cout << "a++  " <<"= "<< a++<<endl;
    cout << "a--  " <<"= "<< a--<<endl;
    cout << "++a  " <<"= "<< ++a<<endl;
    cout << "--a  " <<"= "<< --a<<endl;
    return 0;
}